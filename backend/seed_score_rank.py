def seed_score_rank_tables():
    """填充陕西 2024 一分一段表数据"""
    db = SessionLocal()

    # 清除已有数据
    deleted = db.query(ScoreRankTable).filter(
        ScoreRankTable.province == PROVINCE,
        ScoreRankTable.year == YEAR
    ).delete()
    db.commit()
    if deleted:
        print(f"已清除 {deleted} 条旧数据")

    # 填充文科数据
    arts_count = 0
    for score, count, cumulative in ARTS_DATA:
        row = ScoreRankTable(
            year=YEAR, province=PROVINCE, category="文科",
            score=score, cumulative_count=cumulative, count_this_score=count
        )
        db.add(row)
        arts_count += 1

    # 填充理科数据
    science_count = 0
    for score, count, cumulative in SCIENCE_DATA:
        row = ScoreRankTable(
            year=YEAR, province=PROVINCE, category="理科",
            score=score, cumulative_count=cumulative, count_this_score=count
        )
        db.add(row)
        science_count += 1

    db.commit()
    db.close()

    print(f"填充完成：文科 {arts_count} 条，理科 {science_count} 条")
    print(f"文科总计：{ARTS_DATA[-1][2]:,} 人（+缺考/100以下）")
    print(f"理科总计：{SCIENCE_DATA[-1][2]:,} 人（+缺考/100以下）")


if __name__ == "__main__":
    init_db()
    seed_score_rank_tables()
