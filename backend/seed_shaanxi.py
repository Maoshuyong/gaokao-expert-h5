def seed_shaanxi_data():
    """填充陕西省数据"""
    db = SessionLocal()

    # 1. 新增院校（跳过已存在的）
    existing_codes = {c.code for c in db.query(College).all()}
    new_count = 0
    for college in NEW_COLLEGES:
        if college.code not in existing_codes:
            db.add(college)
            new_count += 1
            existing_codes.add(college.code)

    db.commit()
    total_colleges = len(existing_codes)
    print(f"新增 {new_count} 所院校，当前共 {total_colleges} 所")

    # 2. 清空旧的陕西数据（避免重复）
    deleted = db.query(Score).filter(Score.province == "陕西").delete()
    print(f"清空旧的陕西录取数据 {deleted} 条")

    # 3. 插入新的分数线数据
    for score in SCORE_DATA:
        db.add(score)

    db.commit()
    print(f"插入 {len(SCORE_DATA)} 条陕西录取分数线数据")

    # 4. 统计
    arts_count = db.query(Score).filter(Score.province == "陕西", Score.category == "文科").count()
    sci_count = db.query(Score).filter(Score.province == "陕西", Score.category == "理科").count()
    college_count = db.query(Score.college_code).filter(Score.province == "陕西").distinct().count()
    print(f"文科数据: {arts_count} 条, 理科数据: {sci_count} 条, 覆盖院校: {college_count} 所")

    db.close()
    print("\n陕西省数据填充完成！")


if __name__ == "__main__":
    init_db()
    seed_shaanxi_data()
