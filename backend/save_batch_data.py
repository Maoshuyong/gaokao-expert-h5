#!/usr/bin/env python3
"""批量保存一分一段表数据到 CSV"""

import csv
import os
import re

DATA_DIR = os.path.join(os.path.dirname(__file__), 'score_rank_data')
os.makedirs(DATA_DIR, exist_ok=True)

def save_csv(province, category, rows):
    """rows: list of (score, count, cumulative)"""
    filepath = os.path.join(DATA_DIR, f'2024_{province}_{category}.csv')
    with open(filepath, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        writer.writerow(['score', 'count_this_score', 'cumulative_count'])
        for row in rows:
            writer.writerow(row)
    print(f'✅ {province}_{category}: {len(rows)} 条, 最大累计 {rows[-1][2]}, 已保存')

def parse_pipe_data(text):
    """解析 分数|人数|累计人数 格式的文本"""
    rows = []
    seen = set()
    for line in text.strip().split('\n'):
        line = line.strip()
        # 跳过标题行
        if '分数' in line or '人数' in line or not line:
            continue
        # 支持 | 分隔和空格分隔
        parts = re.split(r'[|\s]+', line.replace('，', ','))
        nums = []
        for p in parts:
            p = p.replace(',', '').replace('，', '').strip()
            if re.match(r'^\d+$', p):
                nums.append(int(p))
        if len(nums) >= 3:
            score, count, cum = nums[0], nums[1], nums[2]
            if 100 <= score <= 800 and count > 0 and cum > 0 and score not in seen:
                rows.append((score, count, cum))
                seen.add(score)
    rows.sort(key=lambda x: -x[0])
    return rows


